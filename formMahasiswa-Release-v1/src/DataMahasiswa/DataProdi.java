        String nama_prodi = fldProdi.getText();
        int id = Integer.parseInt(fld_id_prodi.getText());
        try {
            Class.forName(JDBC_DRIVER);
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            stmt = conn.createStatement();
            
            String sql = "UPDATE prodi_mhs SET Nama_Prodi = ? WHERE ID_Prodi = ? ";
            PreparedStatement ps = conn.prepareStatement(sql);
            
            ps.setString(1, nama_prodi);
            ps.setInt(2, id);
            
            ps.execute();
            ps.close();
            stmt.close();
            conn.close();
        }
