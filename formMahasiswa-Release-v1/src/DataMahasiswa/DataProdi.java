    public void showTableData() {
        try {
            Class.forName(JDBC_DRIVER);
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID PRODI");
            model.addColumn("NAMA PRODI");

            stmt = conn.createStatement();
            String sql = "SELECT * FROM prodi_mhs;";
            items.clear();
            rs = stmt.executeQuery(sql);
            while(rs.next()) {
                items.add(new ModelProdi(rs.getInt("ID_Prodi"),rs.getString("Nama_Prodi")));
            }
            int i = 1;
            for (ModelProdi p : items) {
                model.addRow(new Object[]{
                    i,
                    p.getNama_Prodi()
                });
                i++;
            }
            
            rs.close();
            stmt.close();
            tblProdi.setModel(model);
        }
        catch(Exception e) {
            e.printStackTrace();
        }
    }
    
    public void getData() {
        int baris = tblProdi.getSelectedRow();
        fldProdi.setText(items.get(baris).getNama_Prodi());
        fld_id_prodi.setVisible(false);
        fld_id_prodi.setText(Integer.toString(items.get(baris).getID_Prodi()));
    }
    
